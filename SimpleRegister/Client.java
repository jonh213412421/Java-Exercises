//create client struct
public class Client {
	public String name;
	public String phone;
	public String address;
	public int age;
	public String gender;
	public String coments;
	
	public Client (String name, String phone, String address, int age, String gender, String coments) {
		this.name = name;
		this.phone = phone;
		this.address = address;
		this.age = age;
		this.gender = gender;
		this.coments = coments;
	}
}

